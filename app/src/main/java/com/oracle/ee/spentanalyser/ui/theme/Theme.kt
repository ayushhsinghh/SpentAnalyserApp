// ─── Semantic Finance Colors (accessible outside MaterialTheme) ───
@Immutable
data class FinanceColors(
    val credit: Color,
    val creditContainer: Color,
    val debit: Color,
    val debitContainer: Color
)

val LightFinanceColors = FinanceColors(
    credit = CreditGreen,
    creditContainer = CreditGreenLight,
    debit = DebitOrange,
    debitContainer = DebitOrangeLight
)

val DarkFinanceColors = FinanceColors(
    credit = CreditGreenDark,
    creditContainer = CreditGreen,
    debit = DebitOrangeDark,
    debitContainer = DebitOrange
)

val LocalFinanceColors = staticCompositionLocalOf { LightFinanceColors }

